const byName = new Map(LANGUAGES.map((l) => [l.name, l]));

/** Look up a language entry by its name; falls back to English. */
export function getLanguage(name) {
    return byName.get(name) || byName.get('English');
}

/** True when the text uses a formal register in any registered language. */
export function isFormal(text) {
    return LANGUAGES.some((l) => l.formality.test(text));
}

/** Union of every language's stopwords — used for language-agnostic top-word filtering. */
export const ALL_STOPWORDS = new Set(LANGUAGES.flatMap((l) => l.stopwords));

/** Every AI self-reveal pattern across all languages (detect reveals in any tongue). */
export const ALL_AI_REVEAL_PATTERNS = LANGUAGES.flatMap((l) => l.aiReveal);
